package day_3;

public class DoWhileLoop {

	public static void main(String[] args) {

		/*
		 * int i=2;
		 * 
		 * do { System.out.println(i); i++;
		 * 
		 * }while(i<=10);
		 */

		int i = 20;  //initilization 

		do {
			System.out.println(i);
			i++;  //increment

		} while (i <= 10);  //condition

	}

}
