package practice_java;

public class DataType {
	static boolean b=true;
	       boolean d=false;
	      int a=10;
	      char c= 'g';
	   float f=12.54f;
	   double t=12.54;
	   short s=12345;
	   long l=123456789;
	   String str="Uday";
	   byte r=12;
	   
	   public static void main(String[] args) {
		
		   DataType dt=new DataType();
		   System.out.println(dt.b);
		   System.out.println(dt.d);
		   System.out.println(dt.a);
		   System.out.println(dt.c);
		   System.out.println(dt.f);
		   System.out.println(dt.t);
		   System.out.println(dt.s);
		   System.out.println(dt.l);
		   System.out.println(dt.str);
		   System.out.println(dt.r);
	   
	   
	   }   

}
